#include <stdio.h>

#define HEIGHT 10
#define WIDTH 10

void drawSurface(char surface[HEIGHT][WIDTH]) 
{
    for (int i = 0; i < HEIGHT; i++) 
	{
        for (int j = 0; j < WIDTH; j++) 
		{
            printf("%c ", surface[i][j]);
        }
        printf("\n");
    }
}

int main() {
    char surface[HEIGHT][WIDTH] = 
	{
        {'#', '#', '#', '#', '#', '#', '#', '#', '#', '#'},
        {'#', '.', '.', '.', '.', '.', '.', '.', '.', '#'},
        {'#', '.', '$', '.', '.', '.', '.', '$', '.', '#'},
        {'#', '.', '.', '.', '.', '.', '.', '.', '.', '#'},
        {'#', '.', '.', '.', '.', '.', '.', '.', '.', '#'},
        {'#', '.', '.', '.', '.', '.', '.', '.', '.', '#'},
        {'#', '.', '|', '.', '.', '.', '.', '|', '.', '#'},
        {'#', '.', '-', '-', '-', '-', '-', '-', '.', '#'},
        {'#', '.', '.', '.', '.', '.', '.', '.', '.', '#'},
        {'#', '#', '#', '#', '#', '#', '#', '#', '#', '#'}
    };

    drawSurface(surface);

    return 0;
}
